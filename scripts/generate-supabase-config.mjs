import { existsSync, readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

function loadEnvLocal() {
  const envPath = join(root, ".env.local");
  if (!existsSync(envPath)) return;
  for (const line of readFileSync(envPath, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    const value = trimmed.slice(eq + 1).trim();
    if (!process.env[key]) process.env[key] = value;
  }
}

loadEnvLocal();
const outPath = join(root, "lib/supabase/config.generated.ts");

const url = (process.env.NEXT_PUBLIC_SUPABASE_URL ?? "").trim();
const key = (
  process.env.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY ??
  process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ??
  ""
).trim();

if (!url || !key) {
  if (existsSync(outPath)) {
    const existing = readFileSync(outPath, "utf8");
    if (/supabase\.co/.test(existing) && /key:\s*"[^"]{10,}"/.test(existing)) {
      console.warn(
        "[supabase-config] Env vars missing; keeping existing config.generated.ts"
      );
      process.exit(0);
    }
  }
}

const contents = `// Auto-generated by scripts/generate-supabase-config.mjs — do not edit
export const generatedSupabaseConfig = {
  url: ${JSON.stringify(url)},
  key: ${JSON.stringify(key)},
};
`;

writeFileSync(outPath, contents);

if (!url || !key) {
  console.warn(
    "[supabase-config] WARNING: URL or key missing. Set NEXT_PUBLIC_SUPABASE_URL and NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY."
  );
} else {
  console.log("[supabase-config] Embedded Supabase credentials for static build.");
}
